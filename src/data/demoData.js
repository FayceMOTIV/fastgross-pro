// Helper pour transformer les prospects en leads pour le Radar
export const transformProspectsToLeads = (prospects) => {
  return prospects.map((p) => ({
    id: p.id,
    firstName: p.firstName,
    lastName: p.lastName,
    email: p.email,
    company: p.name,
    jobTitle: p.contactName,
    status:
      p.status === 'converted'
        ? 'converted'
        : p.status === 'replied'
          ? 'replied'
          : p.status === 'opened'
            ? 'opened'
            : p.status === 'contacted'
              ? 'contacted'
              : 'new',
    score: p.score / 10,
    lastContactedAt: p.lastContactedAt,
    revenue: p.revenue,
    notes: p.notes,
    channels: p.channels,
    sequence: p.sequence,
    reply: p.reply,
  }))
}
