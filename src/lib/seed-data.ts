export function getClientById(id: string) {
  return CLIENTS.find(c => c.id === id);
}

export function getProductById(id: string) {
  return PRODUCTS.find(p => p.id === id);
}

export function getUserById(id: string) {
  return USERS.find(u => u.id === id);
}

export function getOrdersByClientId(clientId: string) {
  return ORDERS.filter(o => o.clientId === clientId);
}

export function getInvoicesByClientId(clientId: string) {
  return INVOICES.filter(i => i.clientId === clientId);
}

export function getClientsByCommercialId(commercialId: string) {
  return CLIENTS.filter(c => c.commercialId === commercialId);
}

export function getOrdersByLivreurId(livreurId: string) {
  return ORDERS.filter(o => o.livreurId === livreurId);
}

export function getProductsByCategory(category: string) {
  return PRODUCTS.filter(p => p.category === category);
}

export function getLowStockProducts() {
  return PRODUCTS.filter(p => p.stock <= p.minStock);
}

export function getPendingOrders() {
  return ORDERS.filter(o => o.status === "pending" || o.status === "confirmed");
}

export function getOverdueInvoices() {
  return INVOICES.filter(i => i.status === "overdue");
}

export function getUnreadAlerts() {
  return ALERTS.filter(a => !a.isRead);
}
