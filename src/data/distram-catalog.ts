// Fonctions utilitaires
export function getProductsByCategory(category: DistramCategory): DistramProduct[] {
  return DISTRAM_CATALOG.filter(p => p.category === category);
}

export function getProductById(id: string): DistramProduct | undefined {
  return DISTRAM_CATALOG.find(p => p.id === id);
}

export function getBestsellers(): DistramProduct[] {
  return DISTRAM_CATALOG.filter(p => p.bestseller);
}

export function getPromotions(): DistramProduct[] {
  return DISTRAM_CATALOG.filter(p => p.promo);
}

export function getLowStockProducts(): DistramProduct[] {
  return DISTRAM_CATALOG.filter(p => p.stock <= p.stockMin);
}

export function searchProducts(query: string): DistramProduct[] {
  const lowerQuery = query.toLowerCase();
  return DISTRAM_CATALOG.filter(p =>
    p.name.toLowerCase().includes(lowerQuery) ||
    p.description.toLowerCase().includes(lowerQuery) ||
    p.ref.toLowerCase().includes(lowerQuery)
  );
}

export function getCatalogStats() {
  const totalProducts = DISTRAM_CATALOG.length;
  const totalValue = DISTRAM_CATALOG.reduce((sum, p) => sum + (p.prixVente * p.stock), 0);
  const lowStockCount = getLowStockProducts().length;
  const promotionCount = getPromotions().length;
  const categoryCounts = DISTRAM_CATEGORIES.map(cat => ({
    ...cat,
    count: getProductsByCategory(cat.id).length,
  }));

  return {
    totalProducts,
    totalValue,
    lowStockCount,
    promotionCount,
    categoryCounts,
  };
}
